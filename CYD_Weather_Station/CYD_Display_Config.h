/*
 * CYD (Cheap Yellow Display) Konfiguration f端r LovyanGFX
 *
 * Hardware: ESP32-2432S028R (CYD)
 * - Display: 2.8" TFT LCD (320x240 Pixel)
 * - Controller: ILI9341
 * - Touch: XPT2046 (Resistive Touch)
 * - MCU: ESP32
 */

#ifndef CYD_DISPLAY_CONFIG_H
#define CYD_DISPLAY_CONFIG_H

#define LGFX_USE_V1
#include <LovyanGFX.hpp>

// I2C Pins f端r externe Sensoren
#define extSDA 22
#define extSCL 27

// CYD Konfiguration
class LGFX : public lgfx::LGFX_Device
{
  lgfx::Panel_ILI9341 _panel_instance;
  lgfx::Bus_SPI _bus_instance;
  lgfx::Light_PWM _light_instance;
  lgfx::Touch_XPT2046 _touch_instance;

public:
  LGFX(void)
  {
    {
      // SPI Bus Konfiguration f端r Display
      auto cfg = _bus_instance.config();

      cfg.spi_host = VSPI_HOST;
      cfg.spi_mode = 0;
      cfg.freq_write = 40000000; // 40MHz
      cfg.freq_read = 16000000;  // 16MHz
      cfg.spi_3wire = true;
      cfg.use_lock = true;
      cfg.dma_channel = SPI_DMA_CH_AUTO;

      // CYD Display SPI Pins
      cfg.pin_sclk = 14;
      cfg.pin_mosi = 13;
      cfg.pin_miso = 12;
      cfg.pin_dc = 2;

      _bus_instance.config(cfg);
      _panel_instance.setBus(&_bus_instance);
    }

    {
      // Backlight Konfiguration
      auto cfg = _light_instance.config();

      cfg.pin_bl = 21;
      cfg.invert = false;
      cfg.freq = 44100;
      cfg.pwm_channel = 7;

      _light_instance.config(cfg);
      _panel_instance.setLight(&_light_instance);
    }

    {
      // Display Panel Konfiguration
      auto cfg = _panel_instance.config();

      cfg.pin_cs = 15;
      cfg.pin_rst = -1;
      cfg.pin_busy = -1;

      cfg.panel_width = 240;
      cfg.panel_height = 320;

      cfg.offset_x = 0;
      cfg.offset_y = 0;
      cfg.offset_rotation = 0;

      cfg.dummy_read_pixel = 8;
      cfg.dummy_read_bits = 1;
      cfg.readable = true;
      cfg.invert = false;
      cfg.rgb_order = false;
      cfg.dlen_16bit = false;
      cfg.bus_shared = true;

      _panel_instance.config(cfg);
    }

    {
      // Touch Panel Konfiguration (XPT2046)
      auto cfg = _touch_instance.config();

      cfg.x_min = 0;
      cfg.x_max = 239;
      cfg.y_min = 0;
      cfg.y_max = 319;
      cfg.pin_int = 36;
      cfg.pin_rst = -1;
      cfg.bus_shared = true;
      cfg.offset_rotation = 0;

      // SPI Konfiguration f端r Touch (separater Bus)
      cfg.spi_host = HSPI_HOST;
      cfg.freq = 1000000; // 1MHz
      cfg.pin_sclk = 25;
      cfg.pin_mosi = 32;
      cfg.pin_miso = 39;
      cfg.pin_cs = 33;

      _touch_instance.config(cfg);
      _panel_instance.setTouch(&_touch_instance);
    }

    setPanel(&_panel_instance);
  }
};

#endif // CYD_DISPLAY_CONFIG_H
